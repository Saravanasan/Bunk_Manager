package com.example.sarav.anew;

public class listview {
    String subj,date,hr;

    public listview(String subj, String date, String hr) {
        this.subj = subj;
        this.date = date;
        this.hr = hr;
    }

    public String getSubj() {
        return subj;
    }

    public String getDate() {
        return date;
    }

    public String getHr() {
        return hr;
    }

}
